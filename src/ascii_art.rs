extern crate os_type;

pub fn get_ascii_art() -> Vec<&'static str> {
    match os_type::current_platform().os_type {
        os_type::OSType::Arch => {
            return vec![
                "               +                 ",
                "               #                 ",
                "              ###                ",
                "             #####               ",
                "             ######              ",
                "            ; #####;             ",
                "           +##.#####             ",
                "          +##########            ",
                "         #############;          ",
                "        ###############+         ",
                "       #######   #######         ",
                "     .######;     ;###;`\".       ",
                "    .#######;     ;#####.        ",
                "    #########.   .########`      ",
                "   ######'           '######     ",
                "  ;####                 ####;    ",
                "  ##'                     '##    ",
                " #'                         `#   "
            ];
        }
        _ => {
            return vec![
            ];
        }
    }
}
